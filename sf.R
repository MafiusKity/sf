e<-c(arg[seq(3,n,3)])
vl<-c(arg[seq(2,n,3)])
vv<-unlist(arg[seq(1,n,3)])
i<-1
u<-0

repeat{k<-D(expr,vv[i])

o<-((eval(k)*unlist(e[i]))^2)

if(i==1){
    u<-o} else{
        u<-u+o}
i<-1+i
if(i==1+n/3){
    break}
}
f<-eval(expr)
sf<-sqrt(u)
return(cbind(f,sf))
}
